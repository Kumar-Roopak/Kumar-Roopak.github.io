//Loops and Iteration


//For Loop
for (var i =0; i < 11; i++) {
    console.log(i);
}

var roopak = ['Roopak', 'kumar', 1990, 'student', true];
for (var i = 0; i < roopak.length; i++) {
    console.log(roopak[i]);
}


//While Loop

var i = 0;
while(i < roopak.length) {
    console.log(roopak[i]);
    i++;
}